package day5.conditionstatements;

public class Example10 {

	public static void main(String[] args) {
		System.out.println("Program starts");
		int num=1;
		while(num<=3) {
			System.out.println(num);
			num++;
		}
		System.out.println("**************************");
		num=3;
		while(num>=1) {
			System.out.println(num);
			num--;
		}
		System.out.println("Program ends");
	}

}
