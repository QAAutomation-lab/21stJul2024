package day2.localvariables;

public class LocalVariableExample7 {

	public static void main(String[] args) {
		System.out.println("Program Starts");
		// + | - | * | / | %
		double num1=17;
		int num2=5;
		double result=num1/num2;
		System.out.println("Number1: "+num1);//17.0
		System.out.println("Number2: "+num2);//5
		System.out.println("Result: "+result);
		System.out.println("Program ends");
	}
}
/*
	completeNumber/completeNumber=completeNumber
	
	incompleteNumber/completeNumber=incompleteNumber
	
	completeNumber/incompleteNumber=incompleteNumber
*/